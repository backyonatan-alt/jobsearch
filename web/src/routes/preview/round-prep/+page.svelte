<script>
  // Static mockup of the per-round interview prep selector (no backend).
  // Mirrors web/src/routes/app/[id]/+page.svelte's prep section.

  // Two rounds at the same company. Round 1 (Oran) has prep; round 2 (you)
  // does not yet — selecting it lands on "generate for this round", NOT Oran's
  // brief. "General" holds the legacy single-dossier prep after migration.
  const interviews = [
    { id: 100, label: 'in 2 days · Hiring manager', interviewer: 'Oran Levi', hasPrep: true },
    { id: 101, label: 'in 6 days · Team interview', interviewer: 'Dana Cohen', hasPrep: false }
  ];

  let selectedRoundId = $state(100); // defaults to the next upcoming round
  let interviewerInput = $state('');

  const selectedRound = $derived(
    selectedRoundId === null ? null : interviews.find(iv => iv.id === selectedRoundId)
  );
  // Round 1 has prep; General has the legacy brief; round 2 does not.
  const showBrief = $derived(
    selectedRoundId === null || (selectedRound && selectedRound.hasPrep)
  );
  const briefPerson = $derived(selectedRoundId === null ? 'Oran Levi' : (selectedRound?.interviewer || ''));

  function selectRound(id) {
    selectedRoundId = id;
    const iv = interviews.find(x => x.id === id);
    interviewerInput = (iv && !iv.hasPrep) ? iv.interviewer : '';
  }
</script>

